    private void login(){
        String enteredUser = jTextField1.getText();
        String enteredPassword = new String(jPasswordField1.getPassword());

        if (registeredUsers.containsKey(enteredUser) && registeredUsers.get(enteredUser).equals(enteredPassword)) {
            Utama rezalicious = new Utama();
            rezalicious.setVisible(true);
            this.dispose();
        } else {
            incorrectAttempts++;

            if (incorrectAttempts >= maxIncorrectAttempts) {
                JOptionPane.showMessageDialog(this, "Terlalu banyak percobaan salah. Program akan keluar.", "ERROR", JOptionPane.ERROR_MESSAGE);
                System.exit(0); // Exit the program
            } else {
                JOptionPane.showMessageDialog(this, "Username atau Password salah", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    private void register() {
        String newUser = jTextField1.getText();
        String newPassword = new String(jPasswordField1.getPassword());

        // Check if the username already exists
        if (registeredUsers.containsKey(newUser)) {
            JOptionPane.showMessageDialog(this, "Username already exists. Choose a different username.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Store the new user information
        registeredUsers.put(newUser, newPassword);

        // For simplicity, you can just display a message here
        JOptionPane.showMessageDialog(this, "User registered:\nUsername: " + newUser + "\nPassword: " + newPassword);
    }

    
    
